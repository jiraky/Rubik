package it.univr.rubikcube.model;

/**
 * Cube rotation direction.
 * @author Alessandro Menti
 */
public enum CubeRotation {
    /** Clockwise rotation (as seen from above). */
    CLOCKWISE,
    /** Anticlockwise rotation (as seen from above). */
    ANTICLOCKWISE,
    /** Upwise rotation. */
    UPWISE,
    /** Downwise rotation. */
    DOWNWISE,
    /** Clockwise rotation (as seen from the front). */
    CLOCKWISE_FROM_FRONT,
    /** Anticlockwise rotation (as seen from the front). */
    ANTICLOCKWISE_FROM_FRONT
}
